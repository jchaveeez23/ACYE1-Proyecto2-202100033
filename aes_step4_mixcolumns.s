// --- xtime: w0 -> 2*w0 en GF(2^8) ---
xtime:
    lsl   w1, w0, #1
    uxtb  w1, w1
    tst   w0, #0x80
    b.eq  9f
    mov   w2, #0x1b
    eor   w1, w1, w2
9:  mov   w0, w1
    ret

// --- mul3: w0 -> 3*w0 = (2*w0) ^ w0 ---
