// --- AddRoundKey ---
add_round_key:
    mov   x2, #16
1:  ldrb  w3, [x0]
    ldrb  w4, [x1]
    eor   w3, w3, w4
    strb  w3, [x0]
    add   x0, x0, #1
    add   x1, x1, #1
    subs  x2, x2, #1
    b.ne  1b
    ret

// --- SubBytes ---
sub_bytes:
    mov   x2, #16
2:  ldrb  w3, [x0]
    add   x4, x1, x3, uxtw   // índice seguro en SBox
    ldrb  w5, [x4]
    strb  w5, [x0]
    add   x0, x0, #1
    subs  x2, x2, #1
    b.ne  2b
    ret

// --- ShiftRows ---  (x0=&state, x1=&tmp16)
shift_rows:
    // copia 15..0 (evita leer [#16])
    mov   x2, #15
3:  ldrb  w3, [x0, x2]
    strb  w3, [x1, x2]
    subs  x2, x2, #1
    b.pl  3b

    // fila0
    ldrb w3,[x1,#0];  strb w3,[x0,#0]
    ldrb w3,[x1,#4];  strb w3,[x0,#4]
    ldrb w3,[x1,#8];  strb w3,[x0,#8]
    ldrb w3,[x1,#12]; strb w3,[x0,#12]
